-- SQL Script to create history tables for 11 new casino games
-- Aligned with root database schema (1.db/local.sql)
-- Run these in your database (e.g., via phpMyAdmin)

-- Casino War
CREATE TABLE IF NOT EXISTS history_war (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Iduser INT NOT NULL,
    Bet DECIMAL(30,2) NOT NULL,
    Result VARCHAR(255) NOT NULL,
    WinAmount DECIMAL(30,2) NOT NULL,
    Time DATETIME NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

