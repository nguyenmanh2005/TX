            const form = event.target;
            const submitBtn = form.querySelector('button[type="submit"]');
            const r1 = document.getElementById('r1');
            const r2 = document.getElementById('r2');
            const r3 = document.getElementById('r3');
            const message = document.querySelector('#gameMessage');
            const betInput = document.getElementById('betInput');
            const bet = betInput.value;

            if (!bet || bet <= 0) {
                alert('Vui lòng nhập gtlm cược hợp lệ');
                return;
            }

            // Disable button
            submitBtn.disabled = true;
            submitBtn.textContent = 'Đang quay... 🎰';

            // Reset message
            message.className = 'message';
            message.textContent = '🎰 Đang quay...';

            // Start spinning animation
            r1.classList.add('spinning');
            r2.classList.add('spinning');
            r3.classList.add('spinning');

            // Button press effect
            if (typeof GameEffects !== 'undefined') {
                GameEffects.buttonPressEffect(submitBtn);

                // Particle explosion at button
                const rect = submitBtn.getBoundingClientRect();
                GameEffects.createParticleExplosion(rect.left + rect.width / 2, rect.top + rect.height / 2, 20);
            }

            // Spin reels with increasing delay
            const spinCount = 15;
            for (let i = 0; i < spinCount; i++) {
                r1.textContent = symbols[Math.floor(Math.random() * symbols.length)];
                r2.textContent = symbols[Math.floor(Math.random() * symbols.length)];
                r3.textContent = symbols[Math.floor(Math.random() * symbols.length)];
                await delay(50 + i * 10);
            }

            // Stop animation with delay for each reel
            r1.classList.remove('spinning');
            r1.classList.add('stop-animation');
            await delay(200);

            r2.classList.remove('spinning');
            r2.classList.add('stop-animation');
            await delay(200);

            r3.classList.remove('spinning');
            r3.classList.add('stop-animation');
            await delay(300);

            // Remove stop animation
            r1.classList.remove('stop-animation');
            r2.classList.remove('stop-animation');
            r3.classList.remove('stop-animation');

            // Submit form via AJAX
            const formData = new FormData(form);

            // Set AJAX header
            const xhr = new XMLHttpRequest();
            xhr.addEventListener('load', async function () {
                try {
                    const data = JSON.parse(xhr.responseText);

                    if (data.success) {
                        // Update reels with actual results
                        r1.textContent = data.reel1;
                        r2.textContent = data.reel2;
                        r3.textContent = data.reel3;

                        // Update message
                        message.textContent = data.message;
                        message.className = 'message';

                        if (data.status === 'win') {
                            message.classList.add('win');
                            // Add glow effect to reels
                            r1.classList.add('win');
                            r2.classList.add('win');
                            r3.classList.add('win');

                            if (typeof GameEffects !== 'undefined') {
                                GameEffects.addGlowEffect(r1, 2000);
                                GameEffects.addGlowEffect(r2, 2000);
                                GameEffects.addGlowEffect(r3, 2000);

                                // Determine if big win
                                const isBigWin = data.reel1 === '🎭' || data.reel1 === '🍎';
                                if (isBigWin) {
                                    GameEffects.celebrateBigWin(0);
                                } else {
                                    GameEffects.celebrateWin(0);
                                }
                            }
                        } else if (data.status === 'draw') {
                            message.classList.add('draw');
                        } else if (data.status === 'lose') {
                            message.classList.add('lose');
                            if (typeof GameEffects !== 'undefined') {
                                GameEffects.showLoseEffect(document.querySelector('.slot-machine'));
                            }
                        } else if (data.status === 'error') {
                            message.classList.add('lose');
                        }

                        // Update money
                        const moneyDisplay = document.querySelector('.money strong');
                        if (moneyDisplay) {
                            moneyDisplay.textContent = data.money.toLocaleString('vi-VN');
                        }

                        // Update reward display
                        const rewardDisplay = document.querySelector('#rewardDisplay');
                        if (data.reward > 0) {
                            if (!rewardDisplay) {
                                const newRewardDiv = document.createElement('div');
                                newRewardDiv.id = 'rewardDisplay';
                                newRewardDiv.style.cssText = 'font-size: 24px; font-weight: 800; color: #22c55e; margin-top: 10px;';
                                newRewardDiv.textContent = '+' + data.reward.toLocaleString('vi-VN') + ' gtlm';
                                document.querySelector('.slot-machine').appendChild(newRewardDiv);
                            } else {
                                rewardDisplay.textContent = '+' + data.reward.toLocaleString('vi-VN') + ' gtlm';
                                rewardDisplay.style.display = 'block';
                            }
                        } else if (rewardDisplay) {
                            rewardDisplay.style.display = 'none';
                        }

                        // Clear bet input
                        betInput.value = '';

                        // Load history after game
                        await loadGameHistory();

                        // Show results for a moment
                        await delay(2000);
                    } else {
                        message.textContent = data.message;
                        message.className = 'message lose';
                    }

                    // Remove win class from reels
                    r1.classList.remove('win');
                    r2.classList.remove('win');
                    r3.classList.remove('win');

                    // Re-enable button
                    submitBtn.disabled = false;
                    submitBtn.textContent = 'Quay 🎲';

                } catch (error) {
                    console.error('Lỗi:', error);
                    message.textContent = '❌ Có lỗi xảy ra. Vui lòng thử lại!';
                    message.className = 'message lose';
                    submitBtn.disabled = false;
                    submitBtn.textContent = 'Quay 🎲';
                }
            });

            xhr.open('POST', 'ac.php');
            xhr.setRequestHeader('X-Requested-With', 'XMLHttpRequest');
            xhr.send(formData);
        }

        async function loadGameHistory() {
            try {
                const response = await fetch('ac.php?action=get_history', {
                    headers: {
                        'X-Requested-With': 'XMLHttpRequest'
                    }
                });

                const data = await response.json();

                if (data.success && data.history.length > 0) {
                    const historyBox = document.querySelector('.history-box table');

                    if (historyBox) {
                        // Add new row to table
                        const tbody = historyBox.querySelector('tbody') || historyBox;
                        const newRow = document.createElement('tr');
                        const latest = data.history[0];

                        newRow.innerHTML = `
                    <td>${latest.Id}</td>
                    <td>${parseInt(latest.Bet).toLocaleString('vi-VN')}</td>
                    <td>${latest.Result}</td>
                    <td>${parseInt(latest.WinAmount).toLocaleString('vi-VN')}</td>
                    <td>${latest.Time}</td>
                `;

                        // Insert after header if tbody doesn't exist
                        if (!tbody.querySelector('tbody')) {
                            historyBox.appendChild(newRow);
                        } else {
                            tbody.insertBefore(newRow, tbody.firstChild);
                        }

                        // Show message if "Chưa có lượt quay nào"
                        const emptyMsg = document.querySelector('.history-box p');
                        if (emptyMsg) {
                            emptyMsg.style.display = 'none';
                        }
                    }
                }
            } catch (error) {
                console.error('Lỗi load history:', error);
            }
        }

